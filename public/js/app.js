//#region Move Pieces
function playerDrop() {
        player.pos.y += 10;
    if (collide(arena, player)) {
            player.pos.y -= 10;
            shift = "true";
            message();
            setTimeout(() => {playerReset();}, 1);
            merge(arena, player);
            arenaSweep();
            updateScore();
        }
        dropCounter = 0
}
function playerDropHard() {
    while (!collide(arena, player)) {
       player.pos.y++;;
    };

    player.pos.y--;
    shift = "true";
    message();
    setTimeout(() => {playerReset();}, 1);
    merge(arena, player);
    arenaSweep();
    updateScore();
    dropCounter = 0;
};


function playerMove(dir) {
    player.pos.x += dir;
    if (collide(arena, player)) {
        player.pos.x -= dir;
    };
};

function playerRotate(dir) {
    const pos = player.pos.x;
    let offset = 1;
    rotate(player.matrix, dir);
    while (collide(arena, player)) {
        player.pos.x += offset;
        offset = -(offset + (offset > 0 ? 1: -1))
        if (offset > player.matrix[0].length) {
            rotate(player.matrix, -dir);
            player.pos.x = pos;
            return;
        };
    };
};

function rotate(matrix, dir) {
    for (let y = 0; y < matrix.length; ++y) {
        for (let x = 0; x < y; ++x) {
            [

                matrix[x][y],
                matrix[y][x]
            ] = [
                matrix[y][x],
                matrix[x][y],
            ];
        };
    };

    if (dir > 0) {
        matrix.forEach(row => row.reverse());
    }
    else {
        matrix.reverse();
    }
};
