//#region Create Matrix
function createMatrix(w, h) {
    const matrix = [];
    while (h--) {
        matrix.push(new Array(w).fill(0));
    };
    return matrix;
};

function drawMatrix(matrix, offset) {
    matrix.forEach((row, y) => {
        row.forEach((value, x) => {
            if (value !== 0) {
                context2.fillStyle = colors2[value];
                context2.fillRect(x + offset.x,
                                y + offset.y, 
                                1, 1);
            }
        });
    });
};
