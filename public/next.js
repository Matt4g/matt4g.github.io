function collide(arena2, player2) {
    const [m, o] = [player2.matrix, player2.pos]
    for (let y = 0; y < m.length; ++y) {
        for (let x = 0; x < m[y].length; ++x) {
            if (m[y][x] !== 0 &&
                (arena2[y + o.y] &&
                arena2[y + o.y][x + o.x]) !==0) {
                    return true; 
                }
        }
    }
    return false;
}

